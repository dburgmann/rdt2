package evaluation.experiment;

/**
 * Enumeration for all different kinds of experiments. The experiment-type is used
 * to easily identify the type of the experiemnt by only using the getType()-method
 * of the experiment.
 * 
 * @author MK
 */
public enum ExperimentType {

	TEST_EXPERIMENT,
	SPARSE_EXPERIMENT,
	SPARSE_EXPERIMENT_TEST,
	BATCH_OPTIMIZATION_TEST
}
